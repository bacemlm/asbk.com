let currentLanguage = localStorage.getItem("language") || "en"

document.addEventListener("DOMContentLoaded", () => {
  console.log("[v0] DOMContentLoaded fired, current language:", currentLanguage)

  updateLanguageDisplay()
  translatePage()
  applyRTL()

  const langToggle = document.getElementById("langToggle")
  console.log("[v0] langToggle element found:", langToggle)

  if (langToggle) {
    langToggle.addEventListener("click", (e) => {
      e.preventDefault()
      e.stopPropagation()

      const languages = ["en", "fr", "ar"]
      const currentIndex = languages.indexOf(currentLanguage)
      const nextIndex = (currentIndex + 1) % languages.length
      currentLanguage = languages[nextIndex]

      console.log("[v0] Language switched to:", currentLanguage)

      localStorage.setItem("language", currentLanguage)
      updateLanguageDisplay()
      translatePage()
      applyRTL()
    })
    console.log("[v0] Event listener attached to langToggle")
  } else {
    console.log("[v0] ERROR: langToggle button not found!")
  }
})

function updateLanguageDisplay() {
  const langDisplay = document.getElementById("langDisplay")
  if (langDisplay) {
    const langMap = { en: "EN", fr: "FR", ar: "AR" }
    langDisplay.textContent = langMap[currentLanguage]
    console.log("[v0] Language display updated to:", langMap[currentLanguage])
  }
  document.documentElement.lang = currentLanguage
}

function applyRTL() {
  const htmlElement = document.documentElement
  if (currentLanguage === "ar") {
    htmlElement.setAttribute("dir", "rtl")
    document.body.style.direction = "rtl"
    document.body.style.textAlign = "right"
    console.log("[v0] RTL applied for Arabic")
  } else {
    htmlElement.setAttribute("dir", "ltr")
    document.body.style.direction = "ltr"
    document.body.style.textAlign = "left"
    console.log("[v0] LTR applied for English/French")
  }
}

function translatePage() {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n")
    const keys = key.split(".")
    let translation = translations[currentLanguage]

    for (const k of keys) {
      translation = translation[k]
    }

    if (translation) {
      element.innerHTML = translation
    }
  })

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder")
    const keys = key.split(".")
    let translation = translations[currentLanguage]

    for (const k of keys) {
      translation = translation[k]
    }

    if (translation) {
      element.placeholder = translation
    }
  })

  console.log("[v0] Page translated to:", currentLanguage)
}
